package com.entity.infor;

public class XianYiInfor {

	private String inforNo; // 情报研判编号
	private String  xianYiCode;// 嫌疑人编号
	private String  xianYiName;// 嫌疑人姓名
	private String  xianYiIDCard;// 嫌疑人身份证号
	public String getInforNo() {
		return inforNo;
	}
	public void setInforNo(String inforNo) {
		this.inforNo = inforNo;
	}
	public String getXianYiCode() {
		return xianYiCode;
	}
	public void setXianYiCode(String xianYiCode) {
		this.xianYiCode = xianYiCode;
	}
	public String getXianYiName() {
		return xianYiName;
	}
	public void setXianYiName(String xianYiName) {
		this.xianYiName = xianYiName;
	}
	public String getXianYiIDCard() {
		return xianYiIDCard;
	}
	public void setXianYiIDCard(String xianYiIDCard) {
		this.xianYiIDCard = xianYiIDCard;
	}
	
}
